# WAP to find min, max and average of elements of a list having numeric data

# min
a_list = [1, 2, 3]
minimum = min(a_list)
print(minimum)

# max
a_list = [1, 2, 3]
maximum = max(a_list)
print(maximum)

# average
a_list = [1, 2, 3]
sum = sum(a_list)
length = len(a_list)
average = sum/length
print(average)